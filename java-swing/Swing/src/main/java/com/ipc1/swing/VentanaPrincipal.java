    private void btnMostrarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarDatosActionPerformed
        
        String nombre = txtNombre.getText();
        String edad = txtEdad.getText();
        
        if(nombre.isBlank() || edad.isBlank()){
            JOptionPane.showMessageDialog(null, "Debes llenar los campos de nombre y edad");
            return;
        }
        
        try{
            
            int edadInt = Integer.parseInt(edad);
            JOptionPane.showMessageDialog(null, "Hola, " + nombre + " tu edad es " + edadInt );
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "La edad debe ser un n√∫mero entero");
        }
        
        
    }//GEN-LAST:event_btnMostrarDatosActionPerformed

